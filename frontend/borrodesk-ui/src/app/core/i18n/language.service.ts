@Injectable({
  providedIn: 'root'
})
export class LanguageService {
  private readonly document = inject(DOCUMENT);
  private readonly storageKey = 'borrodesk.language';
  private readonly languageState = signal<LanguageCode>(this.readInitialLanguage());

  readonly availableLanguages = languageOptions;
  readonly language = this.languageState.asReadonly();
  readonly dateLocale = computed(() => this.languageState() === 'de' ? 'de' : 'en-US');
  readonly htmlLanguage = computed(() => this.languageState() === 'de' ? 'de' : 'en');

  constructor() {
    effect(() => {
      this.document.documentElement.lang = this.htmlLanguage();
    });
  }

  setLanguage(language: string): void {
    if (!this.isLanguageCode(language)) {
      return;
    }

    this.languageState.set(language);
    this.getLocalStorage()?.setItem(this.storageKey, language);
  }

  translate(key: string, params: TranslationParams = {}): string {
    const language = this.languageState();
    const template = translations[language][key] ?? translations.en[key] ?? key;

    return template.replace(/\{(\w+)\}/g, (match, paramKey: string) => {
      const value = params[paramKey];

      return value === undefined ? match : String(value);
    });
  }

  private readInitialLanguage(): LanguageCode {
    const storedLanguage = this.getLocalStorage()?.getItem(this.storageKey);
    if (storedLanguage && this.isLanguageCode(storedLanguage)) {
      return storedLanguage;
    }

    if (typeof navigator !== 'undefined' && navigator.language.toLowerCase().startsWith('de')) {
      return 'de';
    }

    return 'en';
  }

  private isLanguageCode(value: string): value is LanguageCode {
    return value === 'en' || value === 'de';
  }

  private getLocalStorage(): Storage | null {
    if (typeof localStorage === 'undefined') {
      return null;
    }

    try {
      return localStorage;
    } catch {
      return null;
    }
  }
}
